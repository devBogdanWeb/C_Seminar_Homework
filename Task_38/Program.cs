double[] arr = { 3.4, 2.1, 5.2, 1.7, 4.3 }; // Создание массива вещественных чисел

double min = arr[0]; // Инициализация минимального элемента
double max = arr[0]; // Инициализация максимального элемента

foreach (double num in arr)
{
  if (num < min) // Если текущий элемент меньше минимального
  {
    min = num; // Записываем его в переменную минимального элемента
  }
  else if (num > max) // Если текущий элемент больше максимального
  {
    max = num; // Записываем его в переменную максимального элемента
  }
}

double diff = max - min; // Вычисление разницы между максимальным и минимальным элементами

Console.WriteLine($"Разница между максимальным ({max}) и минимальным ({min}) элементами массива: {diff}");